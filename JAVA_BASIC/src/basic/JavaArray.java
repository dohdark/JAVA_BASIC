package basic;

public class JavaArray {
	public static void main(String[] args) {
		// 배열 = 여러 값을 저장할 수 있는 구조
		// int num; 변수선언
		// int[] num; 배열 선언
		int[] num = {10, 15, 20}; // 배열생성 + 초기화
		// index =>   0   1   2
		// num => | 10 | 15 | 20 |
		// num[1]; : num 배열의 2번째 값 출력
		// num[1] = 50; num배열의 2번째에 50을 입력
		
		int[] num2= new int[5];	  // 배열생성 5칸짜리(기본값 초기화)
		// num2 =>| 0 | 0 | 0 | 0 | 0 |
	}

}
